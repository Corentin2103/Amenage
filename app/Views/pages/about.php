        <h1 class="m-4">Aménâge, c'est quoi ?</h1>
        <h2 class="m-4">Le constat de base</h2>
        <p>En 2030, un tiers des Français aura plus de 60 ans. Dans ce contexte, favoriser l’autonomie à domicile des
            seniors le plus longtemps possible devient une préoccupation sociétale et économique majeure. Pour prévenir
            la perte d’autonomie et encourager le maintien à domicile, les personnes âgées et/ou en situation de
            handicap peuvent avoir recours à des aides techniques. </p>
        <p>Les aides techniques permettent à la personne de pallier une perte d’autonomie dans diverses situations du
            quotidien (toilette, mobilité, etc.). Or, les aides techniques ne sont pas suffisamment mobilisées ou pas
            toujours à bon escient. Ainsi, il apparaît que :
        <ul style="text-align:justify;">
            <li>les personnes âgées et/ou handicapées n’ont pas forcément connaissance des aides techniques et peuvent
                avoir du mal à visualiser leur intérêt et/ou leur intégration dans leur domicile.
            </li>
            <li>une démonstration de l’usage de certaines aides techniques est complexe du fait de leur nécessaire
                installation (on ne peut pas juste montrer un interrupteur automatique, il faut pouvoir l’intégrer au
                domicile pour démontrer son intérêt)
            </li>
            <li>les travailleurs sociaux et services à la personne, de par leur formation, ne disposent pas de des
                compétences et connaissances nécessaires à la prescription et préconisation d’aides techniques. Ainsi,
                ils se trouvent bien souvent obligés faire preuve d’ingéniosité pour convaincre les individus des
                bénéfices que pourraient apporter les aides techniques. Si des photos et des explications sont
                nécessaires à cette compréhension, elles ne sont pas toujours suffisantes pour permettre à l’utilisateur
                de projeter de manière concrète l’aide dans son environnement quotidien.
            </li>
            <li>les aides techniques sont mal acceptées parce qu’elles stigmatisent l’usager.</li>
        </ul>
        </p>
        <p>Par ailleurs, il est bien souvent nécessaire que la personne prenne un temps de réflexion et d’acceptation de
            sa perte d’autonomie avant de se procurer une aide technique. En ce sens, il n’existe pas d’outil (au regard
            de l’état de l’art actuel) pour accompagner la personne dans ce processus d’acceptation et d’intégration des
            aides techniques au domicile. </p>
        <p>Aménâge a donc été développé pour fournir un outil numérique de sensibilisation, disponible pour le grand
            public. Aménâge est également pertinent pour les professionnels de santé qui ont pour mission d’accompagner,
            informer, conseiller les personnes fragiles, sans pour autant avoir toutes les informations à portée de
            main. </p>


        <h2 class="m-4">Concrètement, quelles informations je trouve sur Aménâge ?</h2>
        <p>Parce que votre situation est propre à votre parcours de vie, il n'est pas possible d'établir précisément vos
            besoins via des questions standards. Seul un professionnel de l'autonomie est compétent dans la définition
            de vos besoins et dans la préconisation des solutions qui pourront y répondre. En effet, ce sont eux qui
            peuvent vous préconiser des solutions 100% adaptées à votre situation, à votre logement, à votre mode de
            vie.</p>
        <p>Aménâge n'a pas vocation à remplacer cette compétence. Aménâge est un outil préliminaire à l'intervention des
            professionnels de l'autonomie, pour vous éclairer dans les difficultés potentielles que vous pouvez
            rencontrer, et surtout dans le large éventail des solutions qui s'offre à vous. </p>
        <p>Pour cela, Aménâge vous propose 2 fonctionnalités principales : un <a
                    href="https://amenage.i2ml.fr/cibler-mon-besoin" rel="nofollow">outil simplifié de définition des
                besoins</a> et un <a href="https://amenage.i2ml.fr/categories" rel="nofoloow">catalogue de solutions</a>.
        </p>
