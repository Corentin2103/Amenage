        <p>Composé des aides techniques les plus répandues, le catalogue d'Aménâge va vous permettre de naviguer entre
            différentes catégories d'aides techniques pour en sélectionner quelques unes. Pour chaque aide technique,
            Aménâge vous diffuse un certain nombre d'informations telles qu'une brève description, le prix, le montant
            de remboursement, les dimensions... Filtrez avec précision les résultats pour trouver exactement ce dont
            vous avez besoin. </p>
        <p>Ces informations sont celles de la CPAM et celles généralement observées chez les distributeurs d'aides
            techniques : elles résultent d'une veille globale réalisée par l'équipe d'Aménâge pour vous transmettre les
            informations qui, en général, sont diffusées par ces acteurs. C'est par exemple pourquoi vous trouverez des
            fourchettes de prix et non pas un prix précis. De la même manière, vous ne trouverez pas de modèle
            spécifique à commander : Aménâge n'a pas vocation à vous orienter vers un produit, mais plutôt vers une
            catégorie de solutions pour vous permettre de visualiser le type de solution que pourront vous préconiser
            les professionnels compétents.</p>


        <h2 class="m-4">Où avez-vous trouvé ces informations ?</h2>
        <p>Les informations présentes dans Aménâge sont celles qui ont été observées auprès de nombreux distributeurs de
            matériel médical et paramédical et auprès des listes de remboursement de la CPAM. Ce sont des données
            publiques trouvées sur leur site internet. L'équipe d'Aménâge a recensé une grande quantité de ces
            informations afin de vous proposer des informations larges correspondant à la réalité de l'ensemble des
            produits proposés par différents acteurs. Cette recherche a été faite pour plusieurs informations (prix,
            dimensions, descriptions, utilisations...) pour chacune des solutions proposées dans Aménâge. Grâce à cette
            centralisation d'informations, vous gagnez un temps précieux à ne pas comparer une à une les sources
            d'informations existantes. </p>

        <h2 class="m-4">Ok, j'ai mes informations. Et maintenant ?</h2>
        <p>Une fois que vous avez trouvé les informations que vous cherchiez sur Aménâge, vous devriez être en mesure
            d'avoir une idée plus claire de vos besoins et des solutions potentielles associées. Il ne vous reste plus
            qu'à prendre contact avec des professionnels compétents qui sauront évaluer précisément vos besoins et qui
            sauront vous guider dans le choix des solutions pertinentes adaptées à votre situation propre. </p>
        <p>Pour cela, l'équipe d'Aménâge conseille de vous rapprocher de :
        <ul style="text-align:justify;">
            <li> du CCAS ou de la mairie de votre commune de résidence : ils sauront vous guider dans les démarches à
                accomplir.
            </li>
            <li> si vous êtes bénéficiaire de l'APA (Allocation Personnalisée d'Autonomie), vous pouvez vous adresser à
                la personne en charge de votre dossier. Cette dernière saura certainement en mesure de vous proposer un
                accompagnement.
            </li>
            <li> vous pouvez également vous tourner vers un ergothérapeute, libéral ou non, pour que celui-ci évalue vos
                besoins et vous préconise des solutions adaptées.
            </li>
        </ul>
        </p>


