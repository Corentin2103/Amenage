        <h1 class="m-4">Guide d'installation</h1>
        <p>Vous souhaitez contribuer au developpement de la plateforme ? Suivez ce guide pour lancer l'application
            web.</p>
        <ul style="text-align:justify;">
            <li>Clonez le projet <code>git clone https://github.com/i2ml/Amenage.git</code></li>
            <li>Suivez le guide d'installation mannuel de code igniter <a
                        href="https://codeigniter.com/user_guide/installation/installing_manual.html" rel="nofollow">(lien
                    direct)</a>.
            </li>
            <li>Décompressez le fichier public/assets package.zip</li>
            <li>Renommez le fichier <code>env</code> en <code>.env</code> et complétez les champs qui ne sont pas
                commentés à l'aide de la commande <code>nano .env</code></li>
        </ul>

