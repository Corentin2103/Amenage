        <h1 class="m-4">Qui sommes-nous ?</h1>

        <p>Aménâge est une application web permettant d’obtenir simplement et rapidement des informations sur les aides
            techniques en fonction de sa situation (état de santé, environnement existant et difficultés en
            découlant).</p>
        <p>Open-source et gratuite, la solution est accessible à tous à l'adresse <a href="https://amenage.i2ml.fr"
                                                                                     rel="nofollow">https://amenage.i2ml.fr</a>
        </p>
        <p>La plateforme est liée au logiciel d'amenagement Amenage3D, autre projet open-source de la fondation i2ml (<a
                    href="https://github.com/i2ml/Amenage3D">https://github.com/i2ml/Amenage3D</a>), le site permet en
            effet d'obtenir des informations supplémentaires sur les aides techniques présente dans le logiciel.</p>
        <p>Aménâge est développé par la Fondation i2ml et financé par la CFPPA du Gard et le Conseil Départemental du
            Gard. </p>


